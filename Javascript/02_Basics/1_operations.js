// strict vs loose equality

console.log(5=='5'); //true, loose
console.log(5==='5'); // false, strict

// === compares both value and type
// == doesnt compare data types

//Logical Operations

//AND && , OR ||
console.log(5>=5 && 5===5); //true
console.log(5>7 || 7>5); //true
//NOT !
console.log(!(5===4)); // true


// truth = 1, string with length not 0, any other Number, {}, []
// false = 0, null, false, '',NaN, undefined
